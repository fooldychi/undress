/**
 * 获取SVG图标
 * @param {string} name - 图标名称
 * @returns {string|null} SVG字符串
 */
export function getSvgIcon(name) {
  const icon = SVG_ICONS[name]
  return icon ? icon.svg : null
}

/**
 * 获取图标信息
 * @param {string} name - 图标名称
 * @returns {object|null} 图标信息
 */
export function getIconInfo(name) {
  return SVG_ICONS[name] || null
}

/**
 * 获取所有图标列表
 * @returns {Array} 图标列表
 */
export function getAllIcons() {
  return Object.keys(SVG_ICONS).map(key => ({
    key,
    ...SVG_ICONS[key]
  }))
}

/**
 * 按分类获取图标
 * @param {string} category - 分类名称
 * @returns {Array} 图标列表
 */
export function getIconsByCategory(category) {
  return getAllIcons().filter(icon => icon.category === category)
}

/**
 * 注册新图标
 * @param {string} name - 图标名称
 * @param {object} iconData - 图标数据
 */
export function registerIcon(name, iconData) {
  SVG_ICONS[name] = iconData
}

/**
 * 检查图标是否存在
 * @param {string} name - 图标名称
 * @returns {boolean} 是否存在
 */
export function hasIcon(name) {
  return name in SVG_ICONS
}
